public extension String {
    
    /// CategoryColors
    enum CategoryColors: String {
        case CatBlue = "#6666FF"
        case CatRed = "#FF6666"
        case CatGreen = "#66FF66"
        case CatYellow = "#FFFF66"
        case CatOrange = "#FFB366"
        case CatCyan = "#66FFFF"
        case CatPurple = "#CC99FF"
        case CatBrown = "#CC9966"
        case CatMint = "#99FFE5"
        case CatIndigo = "#9999FF"
        case CatTeal = "#66CCCC"
    }
}
